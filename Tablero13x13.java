        // TODO add your handling code here:
          Random random = new Random();
    int dado = random.nextInt(6) + 1; // Lanzar el dado (número aleatorio entre 1 y 6)
      
    int nuevaFila = posicionesJugadores[jugador - 1][0] + dado; // Nueva fila del jugador
    int nuevaColumna = posicionesJugadores[jugador - 1][1]; // La columna se mantiene igual
    
    // Verificar si el jugador ha llegado al final del tablero
    if (nuevaFila >= 15) {
        JOptionPane.showMessageDialog(this, "¡El jugador " + jugador + " ha ganado!");
        return;
    }
    
    // Actualizar la posición del jugador en la matriz
    posicionesJugadores[jugador - 1][0] = nuevaFila;
    
    // Actualizar la ficha del jugador en el tablero
    actualizarTablero();
